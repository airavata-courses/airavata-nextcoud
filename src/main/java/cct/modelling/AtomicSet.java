public class AtomicSet
    extends ArrayList<AtomInterface> {
  private String name = "NO_NAME";

  public AtomicSet() {
  }

  public AtomicSet(String name) {
    this.name = name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getName() {
    return name;
  }

}
